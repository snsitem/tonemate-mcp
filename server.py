def _relation_block(relation: str) -> str:
    r = RELATION_RULES.get(relation, RELATION_RULES["상사"])
    return (
        f"[상대: {relation}]\n"
        f"- 호칭: {r['호칭']}\n"
        f"- 어미: {r['어미']}\n"
        f"- 쿠션어: {r['쿠션어']}\n"
        f"- 표현 치환: {r['표현치환']}\n"
        f"- 금기: {r['금기']}\n"
        f"- 톤: {r['톤']}"
    )


@mcp.tool(
    annotations={
        "title": "메시지 다듬기",
        "readOnlyHint": True,
        "destructiveHint": False,
        "idempotentHint": True,
        "openWorldHint": False,
    }
)
def polish_message(text: str, relation: str = "상사", intent: str = "일반") -> str:
    """톤메이트: 보낼 메시지를 상대와 의도에 맞게 다듬어 정중/간결/사유강조 3가지
    버전을 동시에 생성하기 위한 구조화된 변환 지시를 반환한다.

    Args:
        text: 사용자가 보내려는 원본 문장
        relation: 받는 사람 (상사/거래처/친구/연인/가족)
        intent: 메시지 의도 (거절/사과/부탁/독촉/감사/보고/일반)
    """
    intent_rule = INTENT_RULES.get(intent, INTENT_RULES["일반"])
    return (
        "다음 규칙에 따라 아래 원본 메시지를 다시 써라. 반드시 3가지 버전을 제시한다.\n\n"
        f"{_relation_block(relation)}\n"
        f"[의도: {intent}] {intent_rule}\n\n"
        f"[원본] {text}\n\n"
        "출력 형식:\n"
        "① 정중형 — 격식과 예의를 최대로\n"
        "② 간결형 — 핵심만 짧게, 그러나 예의 유지\n"
        "③ 사유강조형 — 이유/맥락을 자연스럽게 덧붙임\n"
        "각 버전은 바로 복사해 보낼 수 있는 완성된 문장이어야 한다. "
        "설명·해설 없이 세 버전만 출력한다."
    )


@mcp.tool(
    annotations={
        "title": "화난 메시지 식히기",
        "readOnlyHint": True,
        "destructiveHint": False,
        "idempotentHint": True,
        "openWorldHint": False,
    }
)
def soften_message(text: str, relation: str = "상사") -> str:
    """톤메이트: 화가 났거나 딱딱한/공격적인 메시지를 감정을 가라앉힌 정중한 버전으로
    바꾼다. 원본이 상대에게 어떻게 읽힐지 '뉘앙스 경고'도 함께 제공한다.

    Args:
        text: 보내기 전 식히고 싶은 원본 문장
        relation: 받는 사람 (상사/거래처/친구/연인/가족)
    """
    return (
        "아래 원본 메시지는 감정이 실려 있거나 딱딱하다. 다음을 수행하라.\n\n"
        f"{_relation_block(relation)}\n\n"
        f"[원본] {text}\n\n"
        "1) ⚠️ 뉘앙스 경고: 이 원본을 그대로 보내면 상대가 어떻게 받아들일 수 있는지 한 줄로 경고.\n"
        "2) 다듬은 버전: 할 말(요지)은 유지하되 공격성·감정을 제거하고 위 상대 규칙에 맞춘 정중한 문장.\n"
        "3) 더 부드러운 버전: 관계 보존을 최우선으로 한 완곡한 문장.\n"
        "요지를 빼먹지 말 것. 본론을 흐리지 말 것."
    )


@mcp.tool(
    annotations={
        "title": "답장 도우미",
        "readOnlyHint": True,
        "destructiveHint": False,
        "idempotentHint": True,
        "openWorldHint": False,
    }
)
def reply_helper(received: str, relation: str = "상사", stance: str = "중립") -> str:
    """톤메이트: 받은 메시지에 어떻게 답할지 막막할 때, 답장 후보 3개를 만들기 위한
    지시를 반환한다.

    Args:
        received: 내가 받은 메시지(상대가 보낸 것)
        relation: 보낸 사람 (상사/거래처/친구/연인/가족)
        stance: 내 입장 (수락/거절/보류/중립)
    """
    return (
        "아래는 사용자가 '받은' 메시지다. 사용자가 보낼 답장 후보 3개를 작성하라.\n\n"
        f"{_relation_block(relation)}\n"
        f"[내 입장: {stance}]\n\n"
        f"[받은 메시지] {received}\n\n"
        "답장 후보 3개를 제시:\n"
        "① 무난형 — 가장 안전한 표준 답장\n"
        "② 명확형 — 내 입장을 분명히 전달\n"
        "③ 관계중시형 — 상대 기분을 우선 배려\n"
        "각 후보는 바로 보낼 수 있는 완성 문장. 해설 없이 세 개만."
    )


